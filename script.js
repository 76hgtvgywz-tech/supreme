// Функция безопасного обновления текста
function updateElementText(element, text) {
    const childElements = element.children;
    
    if (childElements.length > 0) {
        // Находим первый текстовый узел
        const textNodes = Array.from(element.childNodes).filter(node => 
            node.nodeType === Node.TEXT_NODE && node.textContent.trim() !== ''
        );
        
        if (textNodes.length > 0) {
            textNodes[0].textContent = text;
        } else {
            // Если нет текстового узла, создаем span
            const span = document.createElement('span');
            span.textContent = text;
            element.insertBefore(span, childElements[0]);
        }
    } else {
        // Если нет дочерних элементов, просто обновляем текст
        element.textContent = text;
    }
}

// Функция загрузки языка
function loadLanguage(lang) {
    const translationObj = translations[lang];
    
    if (!translationObj) {
        console.error(`Переводы для языка "${lang}" не найдены.`);
        return;
    }
    
    // Обновляем все элементы с data-i18n атрибутом
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translationObj[key]) {
            // Используем безопасное обновление текста
            updateElementText(element, translationObj[key]);
        }
    });
    
    // Обновляем текущий язык в переключателе
    const langText = {
        'ru': 'Русский',
        'en': 'English',
        'de': 'Deutsch'
    }[lang];
    
    if (langText) {
        const currentLangElement = document.getElementById('currentLang');
        if (currentLangElement) {
            currentLangElement.textContent = langText;
        }
    }
    
    // Обновляем активный пункт в меню
    document.querySelectorAll('.lang-option').forEach(opt => {
        opt.classList.remove('active');
        if (opt.getAttribute('data-lang') === lang) {
            opt.classList.add('active');
        }
    });
    
    // Меняем атрибут lang у html
    document.documentElement.lang = lang;
    
    currentLanguage = lang;
    
    console.log(`Язык изменен на: ${lang}`);
}

